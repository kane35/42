#include "libft.h"

void	ft_putnbr(int nb)
{
	int min;
	int n1;
	int n2;

	min = 0;
	if (nb == -2147483648)
	{
		min = 1;
		nb += 1;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	n1 = nb / 10;
	n2 = nb % 10;
	if (n1 > 0)
		ft_putnbr(n1);
	if (min  == 1)
	{
		n2 += 1;
	}
	ft_putchar(n2 + '0');
}
