void	ft_putchar(char c);

void	ft_putnbr(int nb)
{
	int n1;
	int n2;
	int up;

	up = 0;
	if (nb == -2147483648)
	{
		nb++;
		up = 1;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	n1 = nb % 10;
	n2 = nb / 10;
	if (n2 > 0)
		ft_putnbr(n2);
	if (up == 1)
		n1++;
	ft_putchar(n1 + '0');
}
