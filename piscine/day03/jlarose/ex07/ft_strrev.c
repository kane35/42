char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	at;

	i = 0;
	j = 0;
	while (*str)
		j++;
	while (i < j)
	{
		at = str[i];
		str[i] = str[j];
		str[j] = at;
		i++;
		j--;
	}
	return (str);
}
