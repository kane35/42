#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int at;

	at = *a;
	*a = *b;
	*b = at;
}

int		main(void)
{
	int a;
	int b;

	a = 8;
	b = 9;
	ft_swap(&a, &b);
	printf("%d", b);
	return (0);
}
